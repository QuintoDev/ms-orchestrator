package com.careassistant.orchestrator.dto;

public class UsuarioResponse {

	private String id;
	private String tipo;
	private String nombre;
	private String apellido;

	public String getId() {
		return id;
	}

	public String getTipo() {
		return tipo;
	}

	public String getNombre() {
		return nombre;
	}

	public String getApellido() {
		return apellido;
	}

}
